public class arithmeticOP {
    public static void main(String[] args) {
        int a = 39;
        int b = 10;

        System.out.println(a + b); //49
        System.out.println(a - b); //29
        System.out.println(a * b); //390
        System.out.println(a / b); //Quotient --> 3

        double x = 39;
        double y = 10;

        System.out.println(x / y); //3.9


    }
}

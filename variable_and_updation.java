public class variable_and_updation {
    public static void main(String[] args) {
        int x = 10;
        System.out.println(x);
        //variable ko bad me hum log update v kar sakate hai

        x = 15;
        System.out.println(x);
        x += 10; //x = x + 10
        System.out.println(x);
        x -= 10; //x = x - 10
        System.out.println(x);
        x *= 10; //x = x * 10
        System.out.println(x);
        x /= 10; //x = x / 10
        System.out.println(x);

    }
}
